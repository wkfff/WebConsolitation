using System.Collections.Generic;

namespace Krista.FM.Server.Dashboards.MinSportSupport
{
    public class TotalElements
    {
        private readonly List<int> rowElements;
        private readonly List<int> colElements;

        public TotalElements()
        {
            rowElements = new List<int>();
            colElements = new List<int>();
        }

        public void AddRowElements(int code)
        {
            rowElements.Add(code);
        }

        public void AddColElements(int code)
        {
            colElements.Add(code);
        }

        public List<int>  GetRowElements()
        {
            return rowElements;
        }

        public List<int> GetColElements()
        {
            return colElements;
        }
    }
}
