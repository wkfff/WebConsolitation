using System;
using System.Runtime.Serialization;

namespace Krista.FM.Update.ColdUpdater
{
	public class InstallUpdateException : Exception
	{
        public InstallUpdateException() : base() { }
        public InstallUpdateException(string message) : base(message) { }
        public InstallUpdateException(string message, Exception ex) : base(message, ex) { }
        public InstallUpdateException(SerializationInfo info, StreamingContext context) : base(info, context) { }
	}

    public class GetUpdatesException : Exception
    {
        public GetUpdatesException() : base() { }
        public GetUpdatesException(string message) : base(message) { }
        public GetUpdatesException(string message, Exception ex) : base(message, ex) { }
        public GetUpdatesException(SerializationInfo info, StreamingContext context) : base(info, context) { }
    }

    public class ReadParametersException : Exception
    {
        public ReadParametersException() : base() { }
        public ReadParametersException(string message) : base(message) { }
        public ReadParametersException(string message, Exception ex) : base(message, ex) { }
        public ReadParametersException(SerializationInfo info, StreamingContext context) : base(info, context) { }
    }
}
