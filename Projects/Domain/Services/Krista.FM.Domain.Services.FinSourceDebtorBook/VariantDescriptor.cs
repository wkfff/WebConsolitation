using System;

namespace Krista.FM.Domain.Services.FinSourceDebtorBook
{
    public class VariantDescriptor
    {
// ReSharper disable InconsistentNaming
        private int id;
        private string code;
        private string name;
        private string description;
        private int actualYear;
        private DateTime reportDate;
// ReSharper restore InconsistentNaming

        public VariantDescriptor(int id, string code, string name, string description, int actualYear, DateTime reportDate)
        {
            Id = id;
            ReportDate = reportDate;
            ActualYear = actualYear;
            Description = description;
            Name = name;
            Code = code;
        }

        public int Id
        {
            get { return id; }
            set { id = value; }
        }

        public string Code
        {
            get { return code; }
            set { code = value; }
        }

        public string Name
        {
            get { return name; }
            set { name = value; }
        }

        public string Description
        {
            get { return description; }
            set { description = value; }
        }

        public int ActualYear
        {
            get { return actualYear; }
            set { actualYear = value; }
        }

        public DateTime ReportDate
        {
            get { return reportDate; }
            set { reportDate = value; }
        }
    }
}
