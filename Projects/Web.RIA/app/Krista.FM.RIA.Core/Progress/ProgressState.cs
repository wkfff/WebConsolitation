using System.Text;

namespace Krista.FM.RIA.Core.Progress
{
    public class ProgressState
    {
        public int Percentage { get; set; }

        public string Text { get; set; }

        public override string ToString()
        {
            return new StringBuilder()
                .Append("{percentage:")
                .Append(Percentage)
                .Append(",text:\"")
                .Append(Text)
                .Append("\"}")
                .ToString();
        }
    }
}
