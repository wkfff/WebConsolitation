using System;
using System.Runtime.Serialization;

namespace Krista.FM.RIA.Extensions.Consolidation.Services.Forms.DAL
{
    /// <summary>
    /// Исключение создания хеш-имени для объекта базы данных.
    /// </summary>
    [Serializable]
    public class ReportDataAccessException : Exception
    {
        public ReportDataAccessException()
        {
        }

        public ReportDataAccessException(string message)
            : base(message)
        {
        }

        public ReportDataAccessException(string message, Exception innerException)
            : base(message, innerException)
        {
        }

        protected ReportDataAccessException(SerializationInfo info, StreamingContext context)
            : base(info, context)
        {
        }
    }
}
