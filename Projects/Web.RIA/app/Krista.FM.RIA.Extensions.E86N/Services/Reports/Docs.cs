namespace Krista.FM.RIA.Extensions.E86N.Services.Reports
{
    public class Docs
    {
        /// <summary>
        /// Признак корня дерева ППО(регион)
        /// </summary>
        public bool Root { get; set; }

        public bool Numerable { get; set; }

        /// <summary>
        /// Наименование "ППО или ГРБС"
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Всего учреждений
        /// </summary>
        public int InstitutionCount { get; set; }

        /// <summary>
        /// Всего документов
        /// </summary>
        public int Count { get; set; }

        /// <summary>
        /// Несозданных документов
        /// </summary>
        public int NotCreated { get; set; }

        /// <summary>
        /// Документов в состоянии создан
        /// </summary>
        public int Created { get; set; }

        /// <summary>
        /// Документов на рассмотрении
        /// </summary>
        public int UnderConsideration { get; set; }

        /// <summary>
        /// Документов на доработке
        /// </summary>
        public int OnEditing { get; set; }

        /// <summary>
        /// Документов утверждено
        /// </summary>
        public int Finished { get; set; }

        /// <summary>
        /// Всего незавершенных документов
        /// </summary>
        public int TotalIncomplete { get; set; }

        /// <summary>
        /// Экспортировано документов
        /// </summary>
        public int Exported { get; set; }

        /// <summary>
        /// Доля незавершенных документов
        /// </summary>
        public double PartOfIncomplete { get; set; }

        /// <summary>
        /// Количество документов в стадии подготовки
        /// </summary>
        public int NumberOfPreparation { get; set; }

        /// <summary>
        /// Наличие завершенных или экспортированных документов 
        /// </summary>
        public int PresenceOfCompleted { get; set; }

        /// <summary>
        /// Отсутствуют мероприятия для КН мероприятий
        /// </summary>
        public int NotInspectionActivity { get; set; }

        /// <summary>
        /// Отсутствуют мероприятия(завершено) для КН мероприятий
        /// </summary>
        public int NotInspectionActivityFinished { get; set; }

        /// <summary>
        /// Не доводится ГЗ
        /// </summary>
        public int NotBring { get; set; }

        /// <summary>
        /// Не доводится ГЗ(завершено)
        /// </summary>
        public int NotBringFinished { get; set; }

        /// <summary>
        /// Количество казенный учреждений у которых не созданы ГЗ
        /// </summary>
        public int GovernmentCountNotCreatedDocs { get; set; }
    }
}