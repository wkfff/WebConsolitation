namespace Krista.FM.RIA.Extensions.E86N.Extensions
{
    public static class StringExtensions
    {
        /// <summary>
        /// Проверяет вхождение без учета регистра
        /// </summary>
        public static bool ContainsUppers(this string str, string value)
        {
            return str.ToUpper().Contains(value.ToUpper());
        }
    }
}
