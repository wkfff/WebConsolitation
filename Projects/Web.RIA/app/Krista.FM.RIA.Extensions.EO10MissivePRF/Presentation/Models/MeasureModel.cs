using System;

namespace Krista.FM.RIA.Extensions.EO10MissivePRF.Presentation.Models
{
    public class MeasureModel
    {
        /// <summary>
        /// Идентификатор мероприятия
        /// </summary>
        public int ID { get; set; }
        
        /// <summary>
        /// Идентификатор мероприятия
        /// </summary>
        public int MeasureId { get; set; }

        /// <summary>
        /// Наименование мероприятия
        /// </summary>
        public string MeasureName { get; set; }
        
        /// <summary>
        /// Идентификатор задачи, к которой принадлежит мероприятие
        /// </summary>
        public int? TaskId { get; set; }
        
        /// <summary>
        /// Наименование задачи
        /// </summary>
        public string TaskName { get; set; }
        
        /// <summary>
        /// Срок исполнения мероприятия
        /// </summary>
        public string SpaceDisch { get; set; }
        
        /// <summary>
        /// Идентификатор отчета по мероприятию
        /// </summary>
        public int RelationshipsId { get; set; }
        
        /// <summary>
        /// Содержание отчета по мероприятию
        /// </summary>
        public string RelationshipsReport { get; set; }
        
        /// <summary>
        /// Дата отчета
        /// </summary>
        public int RelationshipsPeriodId { get; set; }

        /// <summary>
        /// Дата отчета
        /// </summary>
        public DateTime RelationshipsDate { get; set; }
    }
}
